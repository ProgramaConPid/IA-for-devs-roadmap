# Tipos de datos

# Enteros
a = 1
b = 2

# Flotantes
c = 1.1

# Complejos
d = 1 + 2j

# Booleanos
print(a == b) # False
print(a != b) # True

# String
e = "Hola Mundo"

# Listas
f = [1, 2, 3, 4, 5]

# Tuplas
g = (1, 2, 3, 4, 5)

# Diccionarios
h = {"a": 1, "b": 2, "c": 3}

# Conjuntos
i = {1, 2, 3, 4, 5}

# Tipos de datos
print(type(a)) # int
print(type(b)) # int
print(type(c)) # float
print(type(d)) # complex
print(type(e)) # str
print(type(f)) # list
print(type(g)) # tuple
print(type(h)) # dict
print(type(i)) # set