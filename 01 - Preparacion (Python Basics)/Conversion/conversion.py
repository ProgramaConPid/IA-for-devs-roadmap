# Conversion de tipos de datos (int a float)
a = 1
b = float(a)
print(a)
print(b)
print(type(a))
print(type(b))

# Conversion de tipos de datos (float a int)
a = 3.4
b = int(a)
print(a)
print(b)
print(type(a))
print(type(b))

# Conversion de tipos de datos (int a string)
integer = 30
string = str(integer)
print(integer)
print(string)
print(type(integer))
print(type(string))

# Conversion de tipos de datos (string a int)
a = "1"
b = int(a)
print(a)
print(b)
print(type(a))
print(type(b))
