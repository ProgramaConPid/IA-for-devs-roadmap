# Operadores de comparacion

# Mayor que
print(10 > 20) # False

# Menor que
print(10 < 20) # True

# Mayor o igual que
print(10 >= 20) # False

# Menor o igual que
print(10 <= 20) # True

# Igual que
print(10 == 20) # False

# Distinto que
print(10 != 20) # True

# Con cadenas de texto
print("hola" == "hola") # True
print("hola" == "adios") # False
print("manzana" < "banana") # True
